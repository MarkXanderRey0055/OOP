class Person {
    protected String Name;
    protected String Address;

    //setters
    public void setName(String Name) {
        this.Name = Name;
    }

    public void setAddress(String Address) {
        this.Address = Address;
    }

    //getters
    public String getName() {
        return Name;
    }

    public String getAddress() {
        return Address;
    }


    
}